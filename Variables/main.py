#Boolean-The boolean data has only two options, True or False, they are not outputed directly they are most used in conditional statements

is_student=True

if is_student:
    print("You haven't graduated yet, you are still a student")
else:
    print("You are no longer a student")
    
for_sale=True

if for_sale:
    print("The item is for sale")
else:
    print("The item is not for sale")
    